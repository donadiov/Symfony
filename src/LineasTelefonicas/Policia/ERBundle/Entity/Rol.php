<?php

namespace LineasTelefonicas\Policia\ERBundle\Entity;

use Doctrine\ORM\Mapping as ORM;

/**
 * Rol
 */
class Rol
{
	private $rolId;
	private $rolNombre;


    /**
     * Get rolId
     *
     * @return integer 
     */
    public function getRolId()
    {
        return $this->rolId;
    }

    /**
     * Set rolNombre
     *
     * @param string $rolNombre
     * @return Roles
     */
    public function setRolNombre($rolNombre)
    {
        $this->rolNombre = $rolNombre;
    
        return $this;
    }

    /**
     * Get rolNombre
     *
     * @return string 
     */
    public function getRolNombre()
    {
        return $this->rolNombre;
    }
}
